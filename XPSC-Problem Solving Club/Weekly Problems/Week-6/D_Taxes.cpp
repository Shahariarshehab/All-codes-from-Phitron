bool isPrime(ll n)
{
    if (n == 1)
    {
        return false;
    }
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            return false;
        }
    }
    return true;
}
void Shaharia_ar_solve()
{
    ll n;
    cin >> n;
    if (isPrime(n))
    {
        cout << 1 << nl;
    }
    else if (n % 2 == 0)
    {
        cout << 2 << nl;
    }
    else
    {
        if (isPrime(n - 2))
        {
            cout << 2 << nl;
        }
        else
        {
            cout << 3 << nl;
        }
    }
}
